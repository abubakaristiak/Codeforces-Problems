void solve() {
    int n; cin >> n;
    string s; cin >> s;
    bool ok=false, res=true, cnt=false;

    int i=0, j=s.size()-1;
    while (i<j)
    {
        if(s[i]!=s[j]){
            if(ok){
                res=false;
            }else{
                cnt=true;
            }
        }else if(cnt){
            ok=true;
        }
        i++;
        j--;
    }
    if(res){
        cyes;
    }else cno;
    
}


